#Operações matemáticas
#adição
print(10 + 6)
#subtração
print(10 - 6)
#Multiplicação
print(10 * 6)
#Divisão
print(10 / 6)
#Divisão por inteiro (diz quantas vezes um número pode ser inserido dentro de outro)
print(10 // 6)
#Módulos (restante de uma divisão)
print(10 % 6)
#Exponenciais (potenciação)
print(10 ** 6)